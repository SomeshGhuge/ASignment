package This_Keyword;

public class Info_this_keyword {
//	It is used to refer current class variables, method and constructor.
//	Note- this keyword is not used in static context
//
//	Usage of “this” keywords:-
	
//	1)	To refer the  current class global variable
//	2)	To invoke current class method(implicitly)
//	3)	To invoke current class constructor -> this()
//	4)	To pass arguments in method call
//	5)	To pass argument in constructor
//	6)	To return the current class instance from method
	
//	for variable it used as -------->   this.(variable);
//	for method it used as ---------->   this.(method name)();
//	for constructor it used as------>   this();


}
