package Operators;

public class Relation_Operator {
	
	// <,>,=,<=,>=,==,!=

	public static void main(String[] args) {
		int a = 2000;// Initializing the local variable
		int b = 3000;
			
		System.out.println(a>b);// greater than(2000>3000) ans is false
		System.out.println(a<b);//smaller than 
		System.out.println(a<=b);//less than or equal to
		System.out.println(a>=b );//greater than or equal to
		System.out.println(a==b);// equal to
		System.out.println(a != b);// not equal to
		
	}

}
