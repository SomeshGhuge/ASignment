package Operators;

public class Unary_Operator {

	public static void main(String[] args) {
		
		int a = 10;
		
		 System.out.println("The value of a >>>>>"+ (a++));// aadhi print puna add 
		 System.out.println("The value of a >>>>>"+ (a--));// aadhi print puna substract
		 System.out.println("The value of a >>>>>"+ (++a));// aadhi add puna print
		 System.out.println("The value of a >>>>>"+ (++a));// aadhi substract puna print

	}

}
