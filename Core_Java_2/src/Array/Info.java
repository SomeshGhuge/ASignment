/*
     Array:---->
     
        It is a collection of similar type of elements which has 
        contiguous memory location.
        
        Java array is an object which contains elements of a similar 
        data type. 
        
        We can store only a fixed set of elements in a Java array.
        Array in Java is index-based, the first element of the array is 
        stored at the 0th index, and 2nd element is stored on 1st index 
        and so on.
        
        Arrays are used to store multiple values in a single variable,
        instead of declaring separate variables for each value

        Advantages :---->
        
       o	Code Optimization: It makes the code optimized, we can retrieve or sort the data efficiently.
       o	An array can store multiple values in a single variable.
       o	Arrays are fast as compared to primitive data types.
       o	Members of the array are stored in consecutive memory locations.
       o	Random access: We can get any data located at an index position.


      Disadvantages:---->
       o	Size Limit: We can store only the fixed size of elements in the array. It doesn't grow its size at runtime. To solve this problem, collection framework is used in Java which grows automatically.
       o	They can only store data of a single type.

There are two types of array as-
     o	Single Dimensional Array
     o	Multidimensional Array
     
     
Syntax to Declare an Array in Java:--->

1.	dataType[] arr; (or)  
2.	dataType []arr; (or)  
3.	dataType arr[];  


Instantiation of an Array in Java:----->

      1.	arrayRefVar=new datatype[size];  


 */

